export function getCelebrityById(id: number): Celebrity | undefined {
  return celebrities.find((celebrity) => celebrity.id === id)
}

export function getFeaturedCelebrities(): Celebrity[] {
  return celebrities.slice(0, 6)
}

export function getCelebritiesByCategory(category: string): Celebrity[] {
  if (category === "all") return celebrities
  return celebrities.filter((celebrity) => celebrity.category.toLowerCase().includes(category.toLowerCase()))
}

export function searchCelebrities(query: string): Celebrity[] {
  const searchTerm = query.toLowerCase()
  return celebrities.filter(
    (celebrity) =>
      celebrity.name.toLowerCase().includes(searchTerm) ||
      celebrity.category.toLowerCase().includes(searchTerm) ||
      celebrity.bio.toLowerCase().includes(searchTerm),
  )
}
export function getCelebritiesByPriceRange(min: number, max: number): Celebrity[] {
  return celebrities.filter((celebrity) => {
    const price = Number.parseInt(celebrity.price.replace(/[$,]/g, ""))
    return price >= min && price <= max
  })
}

export function getCelebritiesByTier(tier: string): Celebrity[] {
  if (tier === "all") return celebrities
  return celebrities.filter((celebrity) => celebrity.tier === tier)
}

export function getCelebritiesByAvailability(availability: string): Celebrity[] {
  if (availability === "all") return celebrities
  return celebrities.filter((celebrity) => celebrity.availability === availability)
}
